a = 30
b = 3

igual = (a == b)
diferente = (a != b)
maior = (a > b)
menor = (a < b)
maior_igual = (a >= b)
menor_igual = (a <= b)

print(f"a é igual a b?: {igual}")
print(f"a é diferente de b?: {diferente}")
print(f"a é maior do que b?: {maior}")
print(f"a é menor do que b?: {menor}")
print(f"a é maior igual que b?: {maior_igual}")
print(f"a é menor igual que b?: {menor_igual}")
